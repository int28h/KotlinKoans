/*
Напишите программу, которая считывает числовую последовательность неопределенной длины 
и выводит наибольший её элемент
*/
import java.util.*

val scan = Scanner(System.`in`)

fun main(args: Array<String>) {
    var max = scan.nextInt()
    while (scan.hasNext()) {
        var n = scan.nextInt()
        if (max < n) max = n
    }
    print(max)
}

/*
Напишите программу, которая считывает числовую последовательность неопределенной длины и 
выводит наибольший её элемент и его первую позицию
*/
import java.util.*

val scan = Scanner(System.`in`)

fun main(args: Array<String>) {
    var max = scan.nextInt()
    var pos = 1;
    var counter = 1;
    while (scan.hasNext()) {
        var n = scan.nextInt()
        counter++
        if (max < n) {
            max = n
            pos = counter
        }
    }
    print(max.toString() + " " + pos)
}

