не уходил в нуль. На вход подаются:

    Начальный баланс кошелька
    История покупок (Последовательность натуральных чисел)

Ваша задача - вывести:

    Если на покупку не хватит денег: "Ошибка, недостаточно средств для списания. 
	Баланс - B руб, нужно - N руб", где B - баланс кошелька на момент совершения операции, 
	N - сумма средств, запрошенных для списания
    Если все операции прошли успешно: "Благодарим за то, что вы пользуетесь услугами нашей компании! 
	Баланс - B руб", где B - баланс кошелька на момент завершения последней операции
*/
import java.util.*

val scan = Scanner(System.`in`)

fun main(args: Array<String>) {
    var balance = scan.nextInt()

    while (scan.hasNext()) {
        val i = scan.nextInt()
        if (balance - i < 0) {
            println("Ошибка, недостаточно средств для списания. Баланс - $balance руб, нужно - $i руб")
            return
        } else if (balance - i >= 0) balance -= i
    }
    println("Благодарим за то, что вы пользуетесь услугами нашей компании! Баланс - $balance руб")
}

